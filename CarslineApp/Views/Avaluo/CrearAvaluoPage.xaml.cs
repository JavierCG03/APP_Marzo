using CarslineApp.Services;
using CarslineApp.ViewModels;

namespace CarslineApp.Views.Avaluo;

public partial class CrearAvaluoPage : ContentPage
{
    private readonly CrearAvaluoViewModel _viewModel;

    public CrearAvaluoPage()
    {
        InitializeComponent();
        _viewModel = new CrearAvaluoViewModel(new ApiService());
        BindingContext = _viewModel;
    }

    // ?? VIN: decodificar en tiempo real al escribir ???????????????????????????????????
    private void OnVinTextChanged(object sender, TextChangedEventArgs e)
    {
        var vin = e.NewTextValue?.ToUpper() ?? string.Empty;
        _viewModel.VinCambiadoCommand.Execute(vin);
    }

    // ?? Marca ?????????????????????????????????????????????????????????????????????????
    private void OnMarcaTextChanged(object sender, TextChangedEventArgs e)
    {
        // El binding Mode=TwoWay ya actualiza BusquedaMarca; el filtrado ocurre en el setter.
        // Este handler cierra dropdowns rivales.
        _viewModel.MostrarSugerenciasModelo = false;
        _viewModel.MostrarSugerenciasVersion = false;
        _viewModel.MostrarSugerenciasAnio = false;
    }

    // ?? Modelo ????????????????????????????????????????????????????????????????????????
    private void OnModeloTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasMarca = false;
        _viewModel.MostrarSugerenciasVersion = false;
        _viewModel.MostrarSugerenciasAnio = false;
    }

    // ?? Versión ???????????????????????????????????????????????????????????????????????
    private void OnVersionTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasMarca = false;
        _viewModel.MostrarSugerenciasModelo = false;
        _viewModel.MostrarSugerenciasAnio = false;
    }

    // ?? Ańo ???????????????????????????????????????????????????????????????????????????
    private void OnAnioTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasMarca = false;
        _viewModel.MostrarSugerenciasModelo = false;
        _viewModel.MostrarSugerenciasVersion = false;
    }

    // ?? Vehículo a cuenta — cierre de dropdowns rivales ??????????????????????????????
    private void OnCuentaMarcaTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasCuentaModelo = false;
        _viewModel.MostrarSugerenciasCuentaVersion = false;
        _viewModel.MostrarSugerenciasCuentaAnio = false;
    }

    private void OnCuentaModeloTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasCuentaMarca = false;
        _viewModel.MostrarSugerenciasCuentaVersion = false;
        _viewModel.MostrarSugerenciasCuentaAnio = false;
    }

    private void OnCuentaVersionTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasCuentaMarca = false;
        _viewModel.MostrarSugerenciasCuentaModelo = false;
        _viewModel.MostrarSugerenciasCuentaAnio = false;
    }

    private void OnCuentaAnioTextChanged(object sender, TextChangedEventArgs e)
    {
        _viewModel.MostrarSugerenciasCuentaMarca = false;
        _viewModel.MostrarSugerenciasCuentaModelo = false;
        _viewModel.MostrarSugerenciasCuentaVersion = false;
    }
}